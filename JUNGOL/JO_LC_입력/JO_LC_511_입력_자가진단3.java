package JO_LC_입력;

public class JO_LC_511_입력_자가진단3 {

	public static void main(String[] args) {
//		두 개의 정수형 변수를 선언하고 값을 대입하여 아래와 같이 출력되는 프로그램을 작성하라.
//		55 - 10 = 45
//		2008 - 1999 = 9
		
		int a = 55;
		int b = 10;
		int c = 2008;
		int d = 1999;
		
		System.out.printf("%d - %d = %d\n", a, b, a-b);
		System.out.printf("%d - %d = %d", c, d, c-d);
		

	}

}
